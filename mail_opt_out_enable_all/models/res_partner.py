from odoo import api, models


class ResPartner(models.Model):
    _inherit = 'res.partner'

    @api.model
    def default_get(self, fields):
        res = super(ResPartner, self).default_get(fields)
        res['opt_out'] = True
        return res

    def init(self):
        self._cr.execute("""
            UPDATE res_partner
            SET opt_out = true
        """)
